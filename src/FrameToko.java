   public void selectData(){
        String kolom[] = {"Nama", "Harga", "Kasir", "Tanggal"};
        DefaultTableModel dtm = new DefaultTableModel(null, kolom);
        String SQL = "SELECT * FROM db_barang";
        ResultSet rs = KoneksiDB.executeQuery(SQL);
        try{
            while(rs.next()){
            String Nama = rs.getString(1);
            String Harga = rs.getString(2);
            String Kasir = rs.getString(3);
            String Tanggal = rs.getString(4);
            
            String data[] = {Nama,Harga,Kasir,Tanggal};
            dtm.addRow(data);
            }
        } catch (SQLException ex){
            Logger.getLogger(FrameToko.class.getName()).log(Level.SEVERE,null,ex);
        } 
        tb1Data.setModel(dtm);
    }
    
    
