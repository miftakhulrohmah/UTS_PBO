    private void bDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bDeleteActionPerformed
        // TODO add your handling code here:
         int baris = tb1Data.getSelectedRow();
        if(baris != -1){
            String Nama = tb1Data.getValueAt(baris, 0).toString();
            String SQL = "DELETE FROM db_barang WHERE nama_brg ='"+Nama+"'";
            int status = KoneksiDB.execute(SQL);
           if (status == 1){
        JOptionPane.showMessageDialog(this,"Berhasil","Sukses",JOptionPane.INFORMATION_MESSAGE);
        } else {
        JOptionPane.showMessageDialog(this,"Tidak berhasil","Gagal", JOptionPane.WARNING_MESSAGE);
        }   
      }else{
        JOptionPane.showMessageDialog(this, "Pilih Baris Data Terlebih dulu", "Eror", JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_bDeleteActionPerformed

    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        // TODO add your handling code here:
        SimpleDateFormat dateFormat=new SimpleDateFormat("yyyy-MM-dd");
        String tanggal=dateFormat.format(Tanggal.getDate());
        if ("".equals(namabrg.getText())||"".equals(hargabrg.getText())||"".equals(namakasir.getText())||"".equals(tanggal)) {
            JOptionPane.showMessageDialog(this, "Harap lengkapi data Anda","Error", JOptionPane.WARNING_MESSAGE);
            
        }
        
            String SQL="INSERT INTO db_barang VALUES"
                    +"('"+namabrg.getText()+"','"
                    +hargabrg.getText()+"','"
                    +namakasir.getText()+"','"
                    +tanggal+"')";
            int status=KoneksiDB.execute(SQL);
            if (status==1) {
                JOptionPane.showMessageDialog(this, "Data berhasil disimpan","Sukses",JOptionPane.INFORMATION_MESSAGE);
                selectData();
            }
            else{
                JOptionPane.showMessageDialog(this, "Data gagal disimpan","Sukses",JOptionPane.WARNING_MESSAGE);
            }
    }//GEN-LAST:event_bSaveActionPerformed

